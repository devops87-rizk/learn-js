// Array with string collection
let color = ['Red', 'Blue', 'Green', 'Yellow']

// Change Array
color[0] = 'black'

// Add Array
color[4] = 'Red'
color[5] = 'Purple'
color[6] = 'Pink'

// Push
color.push('Tosca')

// Unshift
color.unshift('Silver')

// Pop
color.pop()

// Shift
color.shift()

console.log(color)